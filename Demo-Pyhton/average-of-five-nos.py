# Program to find the average of 5 nos
number_1 = int(input("Enter the first number: "))
number_2 = int(input("Enter the second number: "))
number_3 = int(input("Enter the third number: "))
number_4 = int(input("Enter the fourth number: "))
number_5 = int(input("Enter the fifth number: "))
average = (number_1 + number_2 + number_3 + number_4 + number_5) / 5
print (f"The average of 5 numbers is: {average}")